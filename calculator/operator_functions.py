def adding_op(number1 , number2):
    add_result = number1 + number2
    return add_result

def remove_op(number1 , number2):
    remove_result = number1 - number2
    return remove_result

def multiply_op(number1 , number2):
    multiply_result = number1 * number2
    return multiply_result

def divide_op(number1 , number2):
    divide_result = number1 / number2
    return divide_result